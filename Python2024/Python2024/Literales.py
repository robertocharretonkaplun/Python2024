
# Literales de cadena

# Literal de cadena con comillas simples
cadena = 'Hola mundo!'

# Literal de cadena con comillas dobles
cadena = "Hola mundo!"

# Imprimiendo las cadenas
print(cadena)

# Resultado:
# Hola mundo!


# Literal de caracteres

# Literal de carácter
caracter = 'a'

# Imprimiendo el carácter
print(caracter)

# Resultado:
# a


# Literales numéricos

# Enteros

entero = 1

# Imprimiendo el entero
print(entero)

# Resultado:
# 1

# Decimales

decimal = 1.5

# Imprimiendo el decimal
print(decimal)

# Resultado:
# 1.5

# Imaginarios

imaginario = 1j

# Imprimiendo el imaginario
print(imaginario)

# Resultado:
# 1j


# Literales booleanos

# Literal booleano True
booleano = True

# Literal booleano False
booleano = False

# Imprimiendo los booleanos
print(booleano)

# Resultado:
# True


# Colecciones literales

# Listas

lista = [1, 2, 3, 4]

# Imprimiendo la lista
print(lista)

# Resultado:
# [1, 2, 3, 4]

# Tuplas

tupla = (1, 2, 3, 4)

# Imprimiendo la tupla
print(tupla)

# Resultado:
# (1, 2, 3, 4)

# Diccionarios

diccionario = {"nombre": "Juan", "edad": 30}

# Imprimiendo el diccionario
print(diccionario)

# Resultado:
# {'nombre': 'Juan', 'edad': 30}


# Literales especiales

# Literal None
ninguno = None

# Imprimiendo el literal None
print(ninguno)

# Resultado:
# None

# Literal Ellipsis
tres_puntos = "..."

# Imprimiendo el literal Ellipsis
print(tres_puntos)

# Resultado:
# ...
